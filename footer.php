<br>3. El “SERVIDOR PÚBLICO” adoptará respecto de la información objeto de este Acuerdo las mismas medidas de seguridad que adoptaría normalmente con información confidencial propia, evitando en la medida de lo posible su pérdida, robo, desvío o sustracción.
</p>


<p>
<b>TERCERA</b>.- Sin perjuicio de lo estipulado en el presente Acuerdo, ambas partes aceptan que la obligación de confidencialidad no se aplicará en los siguientes casos:

Cuando la información por su naturaleza sea del dominio público;

Cuando la legislación vigente o un mandato judicial exija su divulgación. En ese caso, el “SERVIDOR PÚBLICO” notificará a “LA DIRECCIÓN” o a su superior inmediato tal eventualidad, y

En caso de que el “SERVIDOR PÚBLICO” pueda probar que la información fue desarrollada o recibida legítimamente por terceros, de forma totalmente independiente a su relación con “LA DIRECCIÓN”.
</p>

<p>
<b>CUARTA</b>.- En caso de que la información resulte revelada, divulgada o utilizada por el                     “SERVIDOR PÚBLICO” de cualquier forma distinta al objeto de este Acuerdo, ya sea de forma dolosa o por mera negligencia, habrá que indemnizar a INSTITUTO TAMAULIPECO DE VIVIENDA Y URBANISMO por los daños y perjuicios ocasionados, sin perjuicio de las acciones civiles o penales que puedan corresponder a este último. 
</p>

<p>
<b>QUINTA</b>.- Las partes se obligan a devolver cualquier documentación, antecedentes facilitados en cualquier tipo de soporte y, en su caso, las copias obtenidas de los mismos, que constituyan información amparada por el deber de confidencialidad objeto del presente Acuerdo en el supuesto de que cese la relación entre las partes por cualquier motivo. 
</p>

<p>
<b>SEXTA</b>.- El presente Acuerdo no excluye la responsabilidad del “SERVIDOR PÚBLICO” por el manejo de la información adquirida desde la toma de posesión de su puesto o desempeño de sus funciones, extendiéndose su vigencia hasta un plazo de 6 meses después de finalizada la relación entre las partes la prestación del servicio, o bien, hasta en tanto no se hayan cumplido los fines por los cuales se reservó la información.

