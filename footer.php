<p>C L Á U S U L A S:</p>


<p><b>PRIMERA</b>.- Objeto. El presente Acuerdo se refiere a la información que “LA DIRECCIÓN” proporcione al “SERVIDOR PÚBLICO“, ya sea de forma oral, gráfica, escrita, electrónica o en cualquier otro tipo de documento o circunstancia, con motivo de los trabajos que se están desarrollando o que se van a desarrollar durante su relación laboral en la Dirección Jurídica y Seguridad Patrimonial, información que deberá  ser utilizada exclusivamente para fines laborales y del objeto mismo de la información que se trate.
</p>

<p>
<b>SEGUNDA</b>.- 
<br>1. El “SERVIDOR PÚBLICO” únicamente utilizará la información facilitada por “LA DIRECCIÓN” para el fin mencionado en la estipulación anterior, comprometiéndose siempre a mantener la más estricta confidencialidad respecto de dicha información, advirtiendo de dicho deber de confidencialidad y secreto tanto a sus compañeros de trabajo como a  cualquier persona.

