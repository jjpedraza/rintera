</p>

<p>
<b>SÉPTIMA</b>.- En caso de cualquier conflicto o discrepancia que pueda surgir en relación con la interpretación y/o cumplimiento del presente Acuerdo, las partes se someten expresamente a los Juzgados y Tribunales del Primer Distrito Judicial con sede en Ciudad Victoria, aplicándose la legislación del Estado de Tamaulipas vigente.
</p>

